package loops;

public class While {
public static void main(String[] args) {
	int a=1;
	while(a<=5) {
		System.out.println("@*%");
		a++;
	}
	int i=1;
	while(i<=4) {
		System.out.println("&");
		i++;
	}
	int n=1;
	while(n<=5) {
		System.out.println("*");
		n++;
	}
	
	
			
}
}
